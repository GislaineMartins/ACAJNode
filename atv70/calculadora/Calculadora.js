export class Calculadora{
    numeroUm;
    numeroDois;


    somar(numeroUm, numeroDois){
        return numeroUm + numeroDois;
    }

    subtracao(numeroUm, numeroDois){
        return numeroUm - numeroDois;
    }

    multiplicacao(numeroUm, numeroDois){
        return numeroUm * numeroDois;
    }

    divisao(numeroUm, numeroDois){
        return numeroUm / numeroDois;
    }
    
    
    


}