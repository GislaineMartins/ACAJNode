export class Pessoa{

    nome;
    cpf;
    idade;

   get nome(){
       return this.nome;
   }

   set nome(nome){
       this.nome = nome;
   }

   get cpf(){
       return this.cpf;
   }

   set cpf(cpf){
       this.cpf = cpf
   }

   get idade(){
       return this.idade;
   }

   set idade(idade){
       this.idade = idade;
   }


}